                        <p>Now considering the importance colour plays in our life it&#39;s given that it should be a major part of the pan industrial revolution we are noticing in today’s world. This is where 3D colour jet printers come into play. Colour jet printers are 3D Printers which involve two basic components, a core and a binder. The core material is spread by a roller into thin layers over the platform. After each layer is spread, the colour binder is jetted selectively from inkjet print heads over the core layer which solidifies the core. This allows the printer to make a full-colour three-dimensional model.</p>
