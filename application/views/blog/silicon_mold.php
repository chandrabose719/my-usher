                        <p>Design Considerations</p>
                        <p>The techniques for silicon mold making are adapted, taking into account different design considerations, such as the type of material (in liquid form) that will be poured into the mold. Aluminum seems to work well with Vacuum forming, while it does not perform very well with Open Face or Multi-Part molding. Additive Manufacturing seems to be the technique of choice when dealing with rapid prototyping, and is known to be compatible with Ceramics, PLA, Resin and can produce an average number of silicon batches without the need to attend to them. Other design considerations to be kept in mind are whether the technique will be able to produce the desired mole which is complex in shape, the cost per part, speed, and how many parts can be made. A technique such as 3D printing, while providing the user with high production speed and low cost, can make a small number of silicon molds which might not be as accurate as say with a technique like LSR, which provides a higher production rate but with a significant increase in cost. </p>
                        <p>Using the proper design technique in silicon molds is widely adopted during on demand manufacturing where complex and accurate shapes are desired. In this regard, silicon molds provide an efficient and low-cost option for manufacturers to achieve the desired product.</p>
