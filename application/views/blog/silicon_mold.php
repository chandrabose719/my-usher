                        <p>Silicon mold making is a simple technique that helps provide a frame of reference for accurate measurement, and shapes of various products, and is primarily used to cater to the on demand manufacturing market. It works by designing a hollow shaped object designed as the outer surface of the product being made, with a liquid that’s poured inward to obtain the desired shape.</p>
                        <p>Manufacturing Process</p>
                        <p>Silicon molds can be manufactured by a number of techniques, with each method having its own advantages and disadvantages, and the best technique is adapted depending on the case scenario. Some of these techniques include rapid prototyping through Additive Manufacturing (known as 3D Printing), CNC milling, Multi-Part molding, LSR and Open Face (1-part) molding along with other similar techniques.</p>
                        <p>The general process involves measuring the outer surface of the object that will be replicated with the mold. Then, the silicon rubber coating is applied along with the polyurethane casting that will make up the mold. It should be noted that the coating should be handled with high care as a difference could result in the replicas coming out with a different shape. Once the silicon is poured, it’s left around the object to finish, with the expected time being dependant on the technique involved. Once the silicon mold is ready, the object is removed and the hollowed surface is examined for surface quality to remove any imperfections, such as air bubbles that could diminish the quality of the mold fill. </p>
