                        <p>After passing through various innovative minds including that of Karl Benz, the wheel has evolved to its modern state, though today its most observable application is in the automotive industry, the wheel is supporting various industries on its back. Today the wheels are much lighter and stronger than the initial automobile wheels. Today they are basically two types of around us for automotive purposes, alloy and steel. The basic difference between the two is that alloys are lightweight as they are made of lighter metals like aluminum and nickel, further, they are better conductors of heat.</p>
                        <p>Wheels recently have become a way of expressing the style quotient of your car, this is usually done by making custom wheels. Few famous custom wheels in the industry are HH- 12, Big Bang Sports BSP31, and 2 Crave.</p>
                        <p>Now, wheels again stand at the brink of evolution as 3D printing opens up doors to the elusive metal of Titanium! Titanium has been the favorite metal of the aerospace industry, but due to cost and complexity reasons, the automotive industry had kept itself to magnesium and aluminum.</p>
                        <p>However, as Arthur C. Clarke’s third law of futurist writing states “Any sufficiently advanced technology is indistinguishable from magic”, 3D printing is no exception. With Electron Beam Melting, the new HRE3D+ wheels concept is paving the way for the future with titanium as its core material. HRE said that the 3D printing process is way faster and efficient. Unlike wherein the traditional process 80% of the material is scraped off while producing the end product, in 3D printing, only 5% is scrapped off, which can be recycled easily for later use.</p>
                        <p>If this 3D printed wheel wasn’t revolutionary enough for you, wait till you check out Goodyear’s “Oxygene”, a tire which by itself defines what we call… “The Future”.</p>
                        <p>Oxygene is made by 3D printed using grounded material formed by recycled tires. The tire is lightweight and shock absorbent thus helping prevents punctures. The tire has moss on its sidewalls which will absorb moisture from the roads and produce oxygen and electricity using photosynthesis. The electricity will be used to power its embedded devices which connect it to the IoT using Li-Fi allowing Vehicle to Vehicle and Vehicle to Infrastructure communication.</p>
                        <p>This along with tires like the “Vision” by Michelin are paving the way to a cleaner greener future and I guess it won’t be long before the “go green” guys would be promoting buying and driving cars!</p>
