                        <p>Injection molding is the manufacturing process adopted for the mass production of fabricated plastic products by most of the manufacturing companies. The main parts required in the process of injection molding consists of a mold, raw plastic material and injection molding machine. General process consists of the mechanism where raw plastic material is heated on the injection molding machine, which is then injected to the mold and takes the desired shape. After this, it is set to cool and solidify so as to get the final product.</p>
