                        <p>Manufacturing Process</p>
                        <p>Injection molding consists of six processing cycles namely clamping, injection, dwelling, cooling, mold opening and the removal or extraction of final product. Out of these six, four processing cycles are the major ones, about which we shall discuss in detail.</p>
                        <p>Clamping: Before injecting material to the mold it is ensured that both halves of the mold must be held securely. For this purpose, one half of the mold is attached to the Injection Unit and other half is allowed to slide. Hydraulically powered clamping unit will then push the molded halves together and exerts the enough force so as to securely close the mold. The time to clamp and close the mold is dependent upon the size of machine and this time can be calculated from the dry cycle time of the machine.</p>
                        <p>Injection: Raw plastic material which is basically in the form of pellets is fed to the Injection Unit. During this process, plastic material is subjected to melt through heat and pressure. This molten plastic would then be fed to the mold and the application of pressure make sure the holding and packing of material.</p>
                        <p>Cooling: Molten plastic inside the mold is then transferred to the Cooling Unit which instantly begins to cool as soon as it makes contact with the interior of the mold. Cooling is done so as to solidify the molten plastic into the desired shape. It may be possible that during the cooling process, shrinking may occur and to avoid this, additional material is fed to the mold in the injection stage so as to minimize the shrinking factor. It must be noted that mold should not be opened until required cooling time is elapsed.</p>
                        <p>Ejection: As soon as the cooling time elapses, the cooled part is subjected to be ejected from the mold with the help of the Ejection Unit. Ejection is done through the opening of mold where a mechanism pushes the final product out of the mold. Force is required to eject the product from the mold, because cooling may result in the adherence of the part of molten plastic to the surface of mold. Another way to avoid this adherence is the spraying of mold release agent onto the mold cavity, before injection.</p>
