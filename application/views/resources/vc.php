                        <ul class="item-block-list">
                            <li>
                                <p>Vacuum casting begins by producing a master model, using a Stereolithography or laser Sintering, with a high-end finished product.</p>
                            </li>
                            <li>
                                <p>After this, the silicone mold is produced by casting silicone around the master copy, partially under vacuum to avoid air bubbles. </p>
                            </li>
                            <li>
                                <p>After curing, the silicone mold is cut according to predefined parting planes removing the master, leaving a cavity to make copies. </p>
