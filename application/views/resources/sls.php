                        <h4>How It Works</h4>
                        <p>Firstly, in a thin layer, the plastic powder is dispersed on the top of a platform inside the build chamber. After that, the powder is preheated to help the laser raise the temperature of particular regions of the powder base, which subsequently helps in solidifying it.</p>
                        <p>The Next step in the process is the creation of a solid part, which is the ultimate motive of the process. It is done by cross-section scanning of the 3D model with the help of the laser at a temperature near to the melting point of the material. After this, layer by layer recoating is done in the build chamber followed by the cross-section scanning. This process continues till the last layer, i.e., completion of a cross-section. Then the parts thus created are cooled down inside the printer. Later, the build chamber is taken away to undergo other works like cleaning of the remaining powder.</p>
