                        <h4>How It Works</h4>
                        <ul class="item-block-list">
                            <li>
                                <p>The process begins by slicing 3D CAD data into layers.</p>
                            </li>
                            <li>
                                <p>The data is then transferred to a machine which constructs the part layer-by-layer upon a build platform.</p>
                            </li>
                            <li>
                                <p>The thermoplastic, made up of reels of thread and the support material are used to create each cross-section of the part.</p>
                            </li>
                            <li>
                                <p>Uncoiled material is slowly extruded through dual heated nozzles.</p>
                            </li>
                            <li>
                                <p>The extrusion nozzles precisely lay down both support and thermoplastic material upon the preceding layers.</p>
                            </li>
                            <li>
                                <p>Extrusion nozzle continues to move in a horizontal X-Y plane while the platform moves down building the part layer by layer.</p>
                            </li>
                            <li>
                                <p>The finished part is removed from the build platform and cleaned of its support material.</p>
                            </li>
                        </ul>
