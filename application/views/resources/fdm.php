                    <div class="item-block-content">
                        <h4>How It Works</h4>
                        <ul class="item-block-list">
                            <li>
                                <p>The process begins by slicing 3D CAD data into layers.</p>
                            </li>
                            <li>
                                <p>The data is then transferred to a machine which constructs the part layer-by-layer upon a build platform.</p>
                            </li>
                            <li>
                                <p>The thermoplastics, made up of reels of thread are used to create each cross-section of the part.</p>
                            </li>
                            <li>
                                <p>Uncoiled material is slowly extruded through heated nozzles.</p>
                            </li>
                            <li>
                                <p>The extrusion nozzles precisely lay down material upon the preceding layers.</p>
                            </li>
                            <li>
                                <p>The finished part is removed from the build platform and cleaned of its support material.</p>
                            </li>
                        </ul>
